let times = 1;
function search() {
    clearInterval(myInterval);
    let lists = [moviesList, seriesList, cartoonsList, dramasList, animesList];
    let listsIndex = 0;
    let containerCatalog = document.getElementById("catalog");
    let containerSlide = document.getElementById("slide");
    let userSearch = document.getElementById("search").value;
    containerCatalog.innerText = "";
    containerSlide.innerText = "";
    containerSlide.style.boxShadow = "none";
    containerSlide.style.backgroundImage = "none";
    containerCatalog.style.background = "none";

    if (times == 1) {
        times++;
    }
    containerSlide.innerHTML = `<p id="search-phrase-result">Resultado(s) da sua busca:</p>`
    let flag = -2;
    let imgIndex;
    let splitTitle;
    for (let i = 0; i < lists[listsIndex].length; i++) {
        splitTitle = lists[listsIndex][i].split(" ")
        if ((splitTitle.includes(userSearch.toLowerCase())) || (userSearch.toLowerCase()) == lists[listsIndex][i]) {
            flag = 1;
            imgIndex = i + 1;
            containerSlide.innerHTML += `<img class="search-result-img" src="${lists[listsIndex][imgIndex]}">`;
        }
        if (i == (lists[listsIndex].length - 1)) {
            i = 0;
            listsIndex++;
        }
        if (listsIndex == lists.length) {
            break;
        }
    }
    if (flag == -2) {
        containerSlide.innerHTML += `<p id="no-found">Nada foi encontrado</p>`;
    }
}


