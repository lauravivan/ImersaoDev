function showOnScreen(list, title, group) {
    let containerCatalog = document.getElementById("catalog");
    let containerSlide = document.getElementById("slide");
    containerCatalog.innerText = "";
    containerCatalog.innerHTML = `<h3 class="catalog-title">${title}</h3>`
    for (let i = 0; i < list.length; i++) {
        if ((i % 2) != 0) { 
            containerCatalog.innerHTML += `<img class="catalog-images" src="${list[i]}"> `;
        }
    }
    clearInterval(myInterval);
    index = 0;
    containerSlide.style.backgroundImage = `url(${group[index].photo})`
    containerSlide.innerHTML = `<h1 id="page-title">${group[index].title}</h1>`;
    myInterval = setInterval(function() {
        index++;
        containerSlide.style.backgroundImage = `url(${group[index].photo})`
        containerSlide.innerHTML = `<h1 id="page-title">${group[index].title}</h1>`;
        if (index == (group.length - 1)) {
            index = -1;
        }
    }, 4580);
}

